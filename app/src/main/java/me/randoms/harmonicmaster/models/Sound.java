public class Sound {
    private int name;
    private int start;
    private int end;

    public int getName() {
        return name;
    }

    public void setName(int name) {
        this.name = name;
    }

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }
}
